def primary_systems_block(workspace_index: int) -> str:
    lines = []
    workspace_label = f"Workspace {workspace_index:02d}"

    for system_index in range(SYSTEMS_PER_WORKSPACE):
        system_id = primary_system_identifier(workspace_index, system_index)
        lines.extend(
            [
                f'{system_id} = softwareSystem "{workspace_label} System {system_index:02d}" "Synthetic software system {system_index:02d} in {workspace_label}." {{',
            ]
        )
        if system_index % 4 == 3:
            lines.append('    tag "Future"')
        if system_index == 0:
            lines.append('    tag "Critical"')

        for container_index, (
            container_slug,
            container_name,
            technology,
            component_names,
        ) in enumerate(CONTAINER_SPECS):
            container_id = primary_container_identifier(
                workspace_index, system_index, container_slug
            )
            lines.extend(
                [
                    f'        {container_id} = container "{container_name}" "{container_name} tier for {workspace_label} System {system_index:02d}." "{technology}" {{',
                    f'            tag "{container_name}"',
                ]
            )
            if container_index == 0:
                lines.append('            tag "EntryPoint"')
            if container_index == 3:
                lines.append('            tag "DataFlow"')

            for component_index, component_name in enumerate(component_names):
                component_id = primary_component_identifier(
                    workspace_index,
                    system_index,
                    container_slug,
                    component_index,
                )
                lines.append(
                    f'            {component_id} = component "{component_name} {component_index:02d}" "{component_name} {component_index:02d} for {workspace_label} System {system_index:02d}." "Rust component"'
                )

            lines.append("        }")

        lines.append("}")
    return "\n".join(lines)


