def reset_directory(path: Path) -> None:
    if path.exists():
        shutil.rmtree(path)
    path.mkdir(parents=True, exist_ok=True)


def write(path: Path, body: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(FILE_HEADER + body.rstrip() + "\n", encoding="utf-8")


def indented_block(body: str, indent: str = "    ") -> list[str]:
    return [f"{indent}{line}" if line else "" for line in body.splitlines()]


def people_block(*, prefix: str, count: int, description_prefix: str) -> str:
    lines = []
