def multi_root_model_block(workspace_index: int) -> str:
    lines = ["model {"]
    people_prefix = f"mr_person_{workspace_index:02d}"

    for person_index in range(MULTI_ROOT_PEOPLE):
        person_id = person_identifier(people_prefix, person_index)
        lines.append(
            f'    {person_id} = person "Multi-root Operator {workspace_index:02d}-{person_index:02d}" "Operator {person_index:02d} for multi-root workspace {workspace_index:02d}."'
        )

    for system_index in range(SYSTEMS_PER_WORKSPACE):
        system_id = multi_root_system_identifier(workspace_index, system_index)
        lines.append(
            f'    {system_id} = softwareSystem "Multi-root Workspace {workspace_index:02d} System {system_index:02d}" "Synthetic multi-root software system {system_index:02d} in workspace {workspace_index:02d}." {{'
        )
        if system_index % 4 == 3:
            lines.append('        tag "Future"')

        for container_index, (
            container_slug,
            container_name,
            technology,
            component_names,
        ) in enumerate(CONTAINER_SPECS):
            container_id = multi_root_container_identifier(
                workspace_index, system_index, container_slug
            )
            lines.extend(
                [
                    f'        {container_id} = container "{container_name}" "{container_name} tier for multi-root workspace {workspace_index:02d} system {system_index:02d}." "{technology}" {{',
                    f'            tag "{container_name}"',
                ]
            )
            if container_index == 0:
                lines.append('            tag "EntryPoint"')

            for component_index, component_name in enumerate(component_names):
                component_id = multi_root_component_identifier(
                    workspace_index,
                    system_index,
                    container_slug,
                    component_index,
                )
                lines.append(
                    f'            {component_id} = component "{component_name} {component_index:02d}" "{component_name} {component_index:02d} for multi-root workspace {workspace_index:02d} system {system_index:02d}." "Rust component"'
                )

            lines.append("        }")

        lines.append("    }")

    lines.append("")
    lines.extend(multi_root_relationship_lines(workspace_index, people_prefix))
    lines.append("")
    lines.extend(multi_root_deployment_lines(workspace_index))
    lines.append("}")
    return "\n".join(lines)


def multi_root_relationship_lines(
    workspace_index: int, people_prefix: str
) -> list[str]:
    lines: list[str] = []

    for system_index in range(SYSTEMS_PER_WORKSPACE):
        person_id = person_identifier(people_prefix, system_index % MULTI_ROOT_PEOPLE)
        system_id = multi_root_system_identifier(workspace_index, system_index)
        ingress_id = multi_root_container_identifier(
            workspace_index, system_index, "ingress"
        )
        api_id = multi_root_container_identifier(workspace_index, system_index, "api")
        worker_id = multi_root_container_identifier(
            workspace_index, system_index, "worker"
        )
        projection_id = multi_root_container_identifier(
            workspace_index, system_index, "projection"
        )
        next_system_index = (system_index + 1) % SYSTEMS_PER_WORKSPACE
        future_suffix = " Future" if system_index % 4 == 3 else ""

        lines.extend(
            [
                f'    {person_id} -> {ingress_id} "Uses" "HTTPS"',
                f'    {ingress_id} -> {api_id} "Routes commands to" "gRPC"',
                f'    {api_id} -> {worker_id} "Schedules background work in" "AMQP"{future_suffix}',
                f'    {api_id} -> {projection_id} "Reads operational state from" "SQL"',
                f'    {system_id} -> {multi_root_system_identifier(workspace_index, next_system_index)} "Depends on" "HTTPS"',
                f'    {multi_root_component_identifier(workspace_index, system_index, "ingress", 0)} -> {multi_root_component_identifier(workspace_index, system_index, "api", 0)} "Delegates commands to" "gRPC"',
                f'    {multi_root_component_identifier(workspace_index, system_index, "api", 1)} -> {multi_root_component_identifier(workspace_index, system_index, "projection", 0)} "Reads projections from" "SQL"',
                f'    {multi_root_component_identifier(workspace_index, system_index, "worker", 1)} -> {multi_root_component_identifier(workspace_index, system_index, "projection", 1)} "Refreshes cached state in" "Kafka"',
            ]
        )

    return lines


